package com.ceiba.transporte.domain.model;

public class Vehiculo {

	private int idVehiculo;
	private int idTonelaje;
	private String placaVehi;
	private int modeloVehi;
	private String soatVehi;
	private String tecnomecanicoVehi;
	private int capacidadVehi;
	public int getIdVehiculo() {
		return idVehiculo;
	}
	public void setIdVehiculo(int idVehiculo) {
		this.idVehiculo = idVehiculo;
	}
	public int getIdTonelaje() {
		return idTonelaje;
	}
	public void setIdTonelaje(int idTonelaje) {
		this.idTonelaje = idTonelaje;
	}
	public String getPlacaVehi() {
		return placaVehi;
	}
	public void setPlacaVehi(String placaVehi) {
		this.placaVehi = placaVehi;
	}
	public int getModeloVehi() {
		return modeloVehi;
	}
	public void setModeloVehi(int modeloVehi) {
		this.modeloVehi = modeloVehi;
	}
	public String getSoatVehi() {
		return soatVehi;
	}
	public void setSoatVehi(String soatVehi) {
		this.soatVehi = soatVehi;
	}
	public String getTecnomecanicoVehi() {
		return tecnomecanicoVehi;
	}
	public void setTecnomecanicoVehi(String tecnomecanicoVehi) {
		this.tecnomecanicoVehi = tecnomecanicoVehi;
	}
	public int getCapacidadVehi() {
		return capacidadVehi;
	}
	public void setCapacidadVehi(int capacidadVehi) {
		this.capacidadVehi = capacidadVehi;
	}
	
	

}
